// Current language state
let currentLanguage = localStorage.getItem('preferredLanguage') || 'en';

// Language names
const languageNames = {
    en: 'English',
    hi: 'हिंदी',
    gu: 'ગુજરાતી'
};

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    applyLanguage(currentLanguage);
    
    // Set initial active state
    document.querySelectorAll('.lang-option').forEach(option => {
        option.classList.remove('active');
        if (option.getAttribute('data-lang') === currentLanguage) {
            option.classList.add('active');
        }
    });
});

// Change language function
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('preferredLanguage', lang);
    applyLanguage(lang);
    
    // Update active state on language options
    document.querySelectorAll('.lang-option').forEach(option => {
        option.classList.remove('active');
        if (option.getAttribute('data-lang') === lang) {
            option.classList.add('active');
        }
    });
    
    toggleLanguageMenu(); // Close menu after selection
}

// Apply language to all elements
function applyLanguage(lang) {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
    
    // Update current language display
    const currentLangElement = document.getElementById('currentLang');
    if (currentLangElement) {
        currentLangElement.textContent = languageNames[lang];
    }
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

// Toggle language menu
function toggleLanguageMenu() {
    const menu = document.getElementById('languageMenu');
    if (menu) {
        menu.classList.toggle('hidden');
    }
}

// Close menu when clicking outside
document.addEventListener('click', function(event) {
    const languageSelector = document.querySelector('.language-selector');
    const languageMenu = document.getElementById('languageMenu');
    
    if (languageSelector && !languageSelector.contains(event.target) && languageMenu) {
        languageMenu.classList.add('hidden');
    }
});

// Get current language
function getCurrentLanguage() {
    return currentLanguage;
}

// Get translation for a key
function translate(key) {
    return translations[currentLanguage][key] || translations['en'][key] || key;
}
